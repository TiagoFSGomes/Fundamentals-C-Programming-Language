#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x, y = 10;


    // Operadores aritméticos são operadores binários de modo geral
    // Operadores de adição e subtração pode operar sobre um único valor (unários)

    x  = - y; // x recebe o valor de y multiplicado por -1
    printf("x = %d\n", x);

    system("pause");
    return 0;
}
